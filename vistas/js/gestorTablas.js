$(document).ready(function(){
	
	/***BUSCADOR CLIENTES O PROSPECTOS****/
	$("#nombreSearch").on("keyup", function(){
		var nombre = $(this).val();
		if (nombre !=="") {
			$.ajax({
				url:"ajax/searchClientes.ajax.php",
				type:"POST",
				cache:false,
				data:{nombre:nombre},
				success:function(data){
					$("#clientesSearch").html(data);
					$("#clientesSearch").fadeIn();
				}  
			});
		}else{
			localStorage.removeItem("idProspecto");
			localStorage.removeItem("nombreProspecto");
			$("#clientesSearch").html("");  
			$("#clientesSearch").fadeOut();
		}
	});

	$("#nombreSearch").keydown(function(e) {
		var key = e.which;
		switch (key) {
			case 13:
				e.preventDefault();
			return false;
				break;
			
			
		}
		
	});
	
	$("#clientesSearch").on("click","li", function(){

		$('#nombreSearch').val($(this).text());
		localStorage.setItem("idProspecto",$(this).val());
		localStorage.setItem("nombreProspecto",$(this).text());
		$('#clientesSearch').fadeOut("fast");
		location.reload();

	});
	if (localStorage.idProspecto === undefined || localStorage.idProspecto === '0') {
		$("#nombreSearch").val();
	}else{
		$('#nombreSearch').val(localStorage.nombreProspecto);
	}
	/***BUSCADOR CLIENTES O PROSPECTOS****/

});